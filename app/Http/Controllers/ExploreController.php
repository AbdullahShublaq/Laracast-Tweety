    {
        //
        $users = User::when($request->search, function ($query) use ($request) {
            return $query->where('name', 'like', '%' . $request->search . '%')
                        ->orWhere('username', 'like', '%' . $request->search . '%');
        })->paginate(10);

//        dd($users);

        return view('explore', compact('users'));
    }

